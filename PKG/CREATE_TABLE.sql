CREATE TABLE MEGAG_DESP_TIPO(
    CODTIPODESPESA   NUMBER GENERATED BY DEFAULT AS IDENTITY,
    DESCRICAO        VARCHAR2(100) NOT NULL,    -- NOME DO TIPO DE DESPESA
    DTAINCLUSAO      DATE DEFAULT SYSDATE,      -- DATA DE INCLUSÃO DO TIPO
    DTAALTERACAO     DATE,                      -- DATA DE ALTERAÇÃO DO TIPO

    CONSTRAINT PK_MEGAG_DESP_TIPO 
        PRIMARY KEY (CODTIPODESPESA)
);

--3°  
CREATE TABLE MEGAG_DESP(
    CODDESPESA          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USUARIOSOLICITANTE  NUMBER       NOT NULL,   -- FK PARA GE_USUARIO(usario logado)
    USUARIOAPROVADOR    NUMBER                   -- FK PARA GE_USUARIO
    CODTIPODESPESA      NUMBER       NOT NULL, 	 -- TIPO DE DESPESA
    PAGO                CHAR(1)      NOT NULL CHECK (PAGO IN ('S','N')),
    VLRRATDESPESA       NUMBER(15,2) NOT NULL,   -- SOMA DO RATEIO
    DESCRICAO           VARCHAR2(200),           -- DESCRICAO DA DESPESA(NÃO DO CENTRO DE CUSTO)
    FORNECEDOR          VARCHAR2(100),
    DTAINCLUSAO         DATE DEFAULT SYSDATE,
    DTAALTERACAO        DATE,
    -- Campos de Reembolso
    CODARQUIVO          NUMBER,                  
    NOMEARQUIVO         VARCHAR2(200),           -- COMPROVANTEREEMBOLSO
    OBSERVACAO          VARCHAR2(100),           
    SEQCENTRORESULTADO  NUMBER(15)   NOT NULL,
    CENTROCUSTO         NUMBER(15)   NOT NULL,   -- FK PARA ABA_CENTRORESULTADO
    STATUS              VARCHAR2(20) CHECK (STATUS IN ('LANCADO','APROVACAO','APROVADO','REJEITADO')),

    CONSTRAINT PK_MEGAG_DESP 
        PRIMARY KEY (CODDESPESA),

    -- FKs
    CONSTRAINT FK_DESPESA_TIPO
        FOREIGN KEY (CODTIPODESPESA)
        REFERENCES MEGAG_DESP_TIPO (CODTIPODESPESA),

    CONSTRAINT FK_REEMBOLSO_SOLIC
        FOREIGN KEY (USUARIOSOLICITANTE)
        REFERENCES GE_USUARIO (SEQUSUARIO),

    CONSTRAINT FK_REEMBOLSO_APROV
        FOREIGN KEY (USUARIOAPROVADOR)
        REFERENCES GE_USUARIO (SEQUSUARIO),

    CONSTRAINT FK_REEMBOLSO_CENTRO
        FOREIGN KEY (SEQCENTRORESULTADO)
        REFERENCES ABA_CENTRORESULTADO (SEQCENTRORESULTADO)
);

--4°
CREATE TABLE MEGAG_DESP_ARQUIVO(
	CODARQUIVO      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CODDESPESA      NUMBER NOT NULL,
    NOMEARQUIVO     VARCHAR2(200) NOT NULL,
    TIPOARQUIVO     VARCHAR2(50),
    DTAINCLUSAO     DATE DEFAULT SYSDATE,
	DTAALTERACAO    DATE,

    CONSTRAINT PK_MEGAG_DESP_ARQUIVO
        PRIMARY KEY (CODARQUIVO),

    CONSTRAINT FK_ARQ_DESPESA
        FOREIGN KEY (CODDESPESA)
        REFERENCES MEGAG_DESP (CODDESPESA)
);







