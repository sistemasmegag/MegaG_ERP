CREATE TABLE MEGAG_DESP_APROVADORES(
    SEQUSUARIO       	NUMBER NOT NULL,  		-- USUÁRIO APROVADOR DA TABELA PERMISSÃO
    CENTROCUSTO      	NUMBER NOT NULL,  		-- CENTRO DE CUSTO VINCULADO TABELA: ABA_CENTRORESULTADO
    SEQCENTRORESULTADO  NUMBER(15) NOT NULL,
    SEQUSUARIOALTERACAO	NUMBER,         		-- USUÁRIO QUE FEZ ALTERAÇÃO VINCULADO CENTRO DE CUSTO TABELA: ABA_CENTRORESULTADO
    NOME				VARCHAR2(100) NOT NULL, -- NOME DO USUÁRIO PARA EXIBIÇÃO
    DTAINCLUSAO      	DATE DEFAULT SYSDATE,
    DTAALTERACAO     	DATE,

    CONSTRAINT PK_TB_APROVADORES
        PRIMARY KEY (SEQUSUARIO, CENTROCUSTO),

    CONSTRAINT FK_APROVADOR_USUARIO
        FOREIGN KEY (SEQUSUARIO)
        REFERENCES GE_USUARIO (SEQUSUARIO),

    CONSTRAINT FK_APROVADOR_CENTROCUSTO
        FOREIGN KEY (SEQCENTRORESULTADO)
        REFERENCES ABA_CENTRORESULTADO (SEQCENTRORESULTADO)
);

