    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE MEGAG_TASK_NOTIFICACOES (
    ID           NUMBER NOT NULL,
    USUARIO      VARCHAR2(100) NOT NULL,
    TIPO         VARCHAR2(40) NOT NULL,
    TITULO       VARCHAR2(255) NOT NULL,
    MENSAGEM     CLOB NOT NULL,
    TASK_ID      NUMBER,
    LIDA         CHAR(1) DEFAULT 'N' NOT NULL,
    CRIADO_EM    DATE DEFAULT SYSDATE,
    LIDA_EM      DATE,

    CONSTRAINT PK_MEGAG_TASK_NOTIFICACOES PRIMARY KEY (ID),
    CONSTRAINT FK_MEGAG_TASK_NOTIF_TASK FOREIGN KEY (TASK_ID)
        REFERENCES MEGAG_TASK_TASKS (ID),
    CONSTRAINT CK_MEGAG_TASK_NOTIF_LIDA CHECK (LIDA IN ('S','N'))
);

CREATE SEQUENCE SEQ_MEGAG_TASK_NOTIFICACOES
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE MEGAG_TASK_LIST_PARTICIPANTES (
    ID           NUMBER GENERATED BY DEFAULT AS IDENTITY,
    LIST_ID       NUMBER NOT NULL,
    LOGINID       VARCHAR2(100) NOT NULL,
    CRIADO_POR    VARCHAR2(100),
    CRIADO_EM     DATE DEFAULT SYSDATE,

    CONSTRAINT PK_MEGAG_TASK_LIST_PART PRIMARY KEY (ID),
    CONSTRAINT UK_MEGAG_TASK_LIST_PART UNIQUE (LIST_ID, LOGINID),
    CONSTRAINT FK_MEGAG_TASK_LIST_PART_LIST FOREIGN KEY (LIST_ID)
        REFERENCES MEGAG_TASK_LISTS (ID)
);


CREATE TABLE MEGAG_TASK_LIST_STATUS (
    ID           NUMBER NOT NULL,
    LIST_ID       NUMBER NOT NULL,
    NOME          VARCHAR2(100) NOT NULL,
    ORDEM         NUMBER DEFAULT 0 NOT NULL,
    COR           VARCHAR2(20),
    ATIVO         CHAR(1) DEFAULT 'S' NOT NULL,
    CRIADO_POR    VARCHAR2(100),
    CRIADO_EM     DATE DEFAULT SYSDATE,

    CONSTRAINT PK_MEGAG_TASK_LIST_STATUS PRIMARY KEY (ID),
    CONSTRAINT UK_MEGAG_TASK_LIST_STATUS UNIQUE (LIST_ID, NOME),
    CONSTRAINT CK_MEGAG_TASK_LIST_STATUS_ATIVO CHECK (ATIVO IN ('S', 'N')),
    CONSTRAINT FK_MEGAG_TASK_LIST_STATUS_LIST FOREIGN KEY (LIST_ID)
        REFERENCES MEGAG_TASK_LISTS (ID)
);

CREATE SEQUENCE SEQ_MEGAG_TASK_LIST_STATUS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;
