CREATE TABLE MEGAG_TASK_SPACES (
    ID           NUMBER NOT NULL,
    NOME         VARCHAR2(200) NOT NULL,
    ATIVO        CHAR(1) DEFAULT 'S' NOT NULL,
    CRIADO_POR   VARCHAR2(100),
    CRIADO_EM    DATE DEFAULT SYSDATE,
    ATUALIZADO_POR VARCHAR2(100),
    ATUALIZADO_EM DATE,

    CONSTRAINT PK_MEGAG_TASK_SPACES PRIMARY KEY (ID),
    CONSTRAINT CK_MEGAG_TASK_SPACES_ATIVO CHECK (ATIVO IN ('S','N'))
);

CREATE SEQUENCE SEQ_MEGAG_TASK_SPACES
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE MEGAG_TASK_LISTS (
    ID           NUMBER NOT NULL,
    SPACE_ID     NUMBER NOT NULL,
    NOME         VARCHAR2(200) NOT NULL,
    ORDEM        NUMBER DEFAULT 0 NOT NULL,
    ATIVO        CHAR(1) DEFAULT 'S' NOT NULL,
    CRIADO_POR   VARCHAR2(100),
    CRIADO_EM    DATE DEFAULT SYSDATE,
    ATUALIZADO_POR VARCHAR2(100),
    ATUALIZADO_EM DATE,

    CONSTRAINT PK_MEGAG_TASK_LISTS PRIMARY KEY (ID),
    CONSTRAINT FK_MEGAG_TASK_LISTS_SPACE FOREIGN KEY (SPACE_ID)
        REFERENCES MEGAG_TASK_SPACES (ID),
    CONSTRAINT CK_MEGAG_TASK_LISTS_ATIVO CHECK (ATIVO IN ('S','N'))
);

CREATE SEQUENCE SEQ_MEGAG_TASK_LISTS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE MEGAG_TASK_TASKS (
    ID             NUMBER NOT NULL,
    LIST_ID        NUMBER NOT NULL,
    TITULO         VARCHAR2(300) NOT NULL,
    DESCRICAO      CLOB,
    STATUS         VARCHAR2(100) DEFAULT 'TODO' NOT NULL,
    PRIORIDADE     VARCHAR2(20) DEFAULT 'MED' NOT NULL,
    TAGS           VARCHAR2(500),
    RESPONSAVEL    VARCHAR2(100),
    DATA_ENTREGA   VARCHAR2(30),
    CRIADO_POR     VARCHAR2(100),
    CRIADO_EM      DATE DEFAULT SYSDATE,
    ATUALIZADO_POR VARCHAR2(100),
    ATUALIZADO_EM  DATE,

    CONSTRAINT PK_MEGAG_TASK_TASKS PRIMARY KEY (ID),
    CONSTRAINT FK_MEGAG_TASK_TASKS_LIST FOREIGN KEY (LIST_ID)
        REFERENCES MEGAG_TASK_LISTS (ID)
);

CREATE SEQUENCE SEQ_MEGAG_TASK_TASKS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE MEGAG_TASK_COMMENTS (
    ID           NUMBER NOT NULL,
    TASK_ID       NUMBER NOT NULL,
    COMENTARIO    CLOB NOT NULL,
    CRIADO_POR    VARCHAR2(100) NOT NULL,
    CRIADO_EM     DATE DEFAULT SYSDATE,

    CONSTRAINT PK_MEGAG_TASK_COMMENTS PRIMARY KEY (ID),
    CONSTRAINT FK_MEGAG_TASK_COMMENTS_TASK FOREIGN KEY (TASK_ID)
        REFERENCES MEGAG_TASK_TASKS (ID)
);

CREATE SEQUENCE SEQ_MEGAG_TASK_COMMENTS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE MEGAG_TASK_FILES (
    ID           NUMBER NOT NULL,
    TASK_ID       NUMBER NOT NULL,
    FILE_NAME     VARCHAR2(255) NOT NULL,
    MIME_TYPE     VARCHAR2(150),
    FILE_SIZE     NUMBER,
    FILE_BLOB     BLOB,
    CRIADO_POR    VARCHAR2(100),
    CRIADO_EM     DATE DEFAULT SYSDATE,

    CONSTRAINT PK_MEGAG_TASK_FILES PRIMARY KEY (ID),
    CONSTRAINT FK_MEGAG_TASK_FILES_TASK FOREIGN KEY (TASK_ID)
