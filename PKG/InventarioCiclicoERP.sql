CREATE TABLE MEGAG_INV_LOTES_APP (
    ID_LOTE              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_PLANO             VARCHAR2(30) NOT NULL,
    STATUS               VARCHAR2(40) NOT NULL,
    PAYLOAD_JSON         CLOB,
    RECEBIDO_EM          DATE DEFAULT SYSDATE NOT NULL,
    USUARIO_RECEBIMENTO  VARCHAR2(80),
    PROCESSADO_EM        DATE,
    MSG_PROCESSAMENTO    VARCHAR2(1000),
    CONSTRAINT PK_MEGAG_INV_LOTES_APP PRIMARY KEY (ID_LOTE),
    CONSTRAINT FK_MEGAG_INV_LOTE_PLANO FOREIGN KEY (ID_PLANO) REFERENCES MEGAG_INV_PLANOS(ID_PLANO)
);

CREATE TABLE MEGAG_INV_CONTAGENS (
    ID_CONTAGEM       NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_PLANO          VARCHAR2(30) NOT NULL,
    CONTAGEM_NUMERO   NUMBER(1) NOT NULL,
    PRODUTIVO         VARCHAR2(80) NOT NULL,
    DISPOSITIVO       VARCHAR2(120),
    INICIO_EM         DATE,
    FIM_EM            DATE,
    STATUS            VARCHAR2(30) DEFAULT 'RECEBIDA' NOT NULL,
    ID_LOTE           NUMBER,
    CONSTRAINT PK_MEGAG_INV_CONTAGENS PRIMARY KEY (ID_CONTAGEM),
    CONSTRAINT FK_MEGAG_INV_CONT_PLANO FOREIGN KEY (ID_PLANO) REFERENCES MEGAG_INV_PLANOS(ID_PLANO),
    CONSTRAINT FK_MEGAG_INV_CONT_LOTE FOREIGN KEY (ID_LOTE) REFERENCES MEGAG_INV_LOTES_APP(ID_LOTE),
    CONSTRAINT CK_MEGAG_INV_CONT_NUM CHECK (CONTAGEM_NUMERO BETWEEN 1 AND 3)
);

CREATE TABLE MEGAG_INV_CONTAGEM_ITENS (
    ID_CONTAGEM_ITEM   NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_CONTAGEM        NUMBER NOT NULL,
    ID_PLANO           VARCHAR2(30) NOT NULL,
    ENDERECO           VARCHAR2(80) NOT NULL,
    CODPRODUTO_BASE    VARCHAR2(60),
    CODPRODUTO_CONTADO VARCHAR2(60),
    DESCRICAO_CONTADA  VARCHAR2(300),
    QUANTIDADE_BASE    NUMBER(18,4),
    QUANTIDADE_CONTADA NUMBER(18,4),
    VALIDADE_BASE      DATE,
    VALIDADE_CONTADA   DATE,
    VALORUNITARIO      NUMBER(18,6),
    DIVERGE_PRODUTO    CHAR(1) DEFAULT 'N',
    DIVERGE_QTD        CHAR(1) DEFAULT 'N',
    DIVERGE_VALIDADE   CHAR(1) DEFAULT 'N',
    VALOR_DIVERGENCIA  NUMBER(18,6),
    INICIO_EM          DATE,
    FIM_EM             DATE,
    CONSTRAINT PK_MEGAG_INV_CONT_ITENS PRIMARY KEY (ID_CONTAGEM_ITEM),
    CONSTRAINT FK_MEGAG_INV_CONT_ITEM FOREIGN KEY (ID_CONTAGEM) REFERENCES MEGAG_INV_CONTAGENS(ID_CONTAGEM)
);

