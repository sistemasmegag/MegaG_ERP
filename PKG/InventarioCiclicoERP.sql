CREATE TABLE MEGAG_INV_PLANOS (
    ID_PLANO              VARCHAR2(30)   NOT NULL,
    DESCRICAO             VARCHAR2(200)  NOT NULL,
    DEPOSITO              VARCHAR2(120)  NOT NULL,
    CONTAGEM_ATUAL        NUMBER(1)      DEFAULT 1 NOT NULL,
    STATUS                VARCHAR2(20)   DEFAULT 'RASCUNHO' NOT NULL,
    GERADO_EM             DATE           DEFAULT SYSDATE NOT NULL,
    LIBERADO_EM           DATE,
    ATUALIZADO_EM         DATE,
    USUARIO_CRIACAO       VARCHAR2(80),
    USUARIO_ATUALIZACAO   VARCHAR2(80),
    USUARIO_LIBERACAO     VARCHAR2(80),
    PRODUTIVO_SEQUSUARIO  NUMBER,
    PRODUTIVO_LOGIN       VARCHAR2(80),
    PRODUTIVO_NOME        VARCHAR2(200),
    OBSERVACAO            VARCHAR2(1000),
    CONSTRAINT PK_MEGAG_INV_PLANOS PRIMARY KEY (ID_PLANO),
    CONSTRAINT CK_MEGAG_INV_PLANOS_STATUS CHECK (STATUS IN ('RASCUNHO', 'LIBERADO', 'EM_CONTAGEM', 'EM_REVISAO', 'FINALIZADO', 'CANCELADO')),
    CONSTRAINT CK_MEGAG_INV_PLANOS_CONT CHECK (CONTAGEM_ATUAL BETWEEN 1 AND 3)
);

CREATE TABLE MEGAG_INV_PLANO_ITENS (
    ID_ITEM          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_PLANO         VARCHAR2(30)  NOT NULL,
    GRUPO_ID         VARCHAR2(30)  NOT NULL,
    RUA              VARCHAR2(20)  NOT NULL,
    LADO             VARCHAR2(10)  NOT NULL,
    ENDERECO         VARCHAR2(80)  NOT NULL,
    CODPRODUTO       VARCHAR2(60)  NOT NULL,
    DESCRICAO        VARCHAR2(300) NOT NULL,
    EANUNIDADE       VARCHAR2(30),
    EANCAIXA         VARCHAR2(30),
    QUANTIDADEBASE   NUMBER(18,4)  DEFAULT 0 NOT NULL,
    UNIDADEBASE      VARCHAR2(20),
    VALIDADEBASE     DATE,
    VALORUNITARIO    NUMBER(18,6)  DEFAULT 0 NOT NULL,
    ATIVO            CHAR(1)       DEFAULT 'S' NOT NULL,
    ORDEM            NUMBER        DEFAULT 0,
    CONSTRAINT PK_MEGAG_INV_PLANO_ITENS PRIMARY KEY (ID_ITEM),
    CONSTRAINT FK_MEGAG_INV_ITEM_PLANO FOREIGN KEY (ID_PLANO) REFERENCES MEGAG_INV_PLANOS(ID_PLANO),
    CONSTRAINT CK_MEGAG_INV_ITEM_LADO CHECK (LADO IN ('IMPAR', 'PAR')),
    CONSTRAINT CK_MEGAG_INV_ITEM_ATIVO CHECK (ATIVO IN ('S', 'N'))
);

CREATE INDEX IX_MEGAG_INV_ITEM_PLANO ON MEGAG_INV_PLANO_ITENS(ID_PLANO, GRUPO_ID, ENDERECO);
CREATE INDEX IX_MEGAG_INV_ITEM_PROD ON MEGAG_INV_PLANO_ITENS(ID_PLANO, CODPRODUTO);

