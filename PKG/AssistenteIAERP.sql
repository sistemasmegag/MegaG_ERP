/* ============================================================
   Modulo ERP - Assistente IA
   Opcional para historico/auditoria persistente do chat.
   A primeira versao da API funciona com historico em sessao.
   ============================================================ */

CREATE TABLE MEGAG_AI_CHAT_SESSOES (
    ID_SESSAO        VARCHAR2(60)   NOT NULL,
    USUARIO          VARCHAR2(80)   NOT NULL,
    MODULO_FOCO      VARCHAR2(80),
    CRIADO_EM        DATE DEFAULT SYSDATE NOT NULL,
    ATUALIZADO_EM    DATE,
    STATUS           VARCHAR2(20) DEFAULT 'ABERTA' NOT NULL,
    CONSTRAINT PK_MEGAG_AI_CHAT_SESSOES PRIMARY KEY (ID_SESSAO),
    CONSTRAINT CK_MEGAG_AI_CHAT_SESS_STATUS CHECK (STATUS IN ('ABERTA', 'ARQUIVADA'))
);

CREATE TABLE MEGAG_AI_CHAT_MENSAGENS (
    ID_MENSAGEM      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_SESSAO        VARCHAR2(60) NOT NULL,
    ROLE             VARCHAR2(20) NOT NULL,
    CONTEUDO         CLOB NOT NULL,
    MODELO           VARCHAR2(80),
    RESPONSE_ID      VARCHAR2(120),
    CRIADO_EM        DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_MEGAG_AI_CHAT_MENSAGENS PRIMARY KEY (ID_MENSAGEM),
    CONSTRAINT FK_MEGAG_AI_CHAT_MSG_SESS FOREIGN KEY (ID_SESSAO) REFERENCES MEGAG_AI_CHAT_SESSOES(ID_SESSAO),
    CONSTRAINT CK_MEGAG_AI_CHAT_MSG_ROLE CHECK (ROLE IN ('user', 'assistant', 'system'))
);

CREATE INDEX IX_MEGAG_AI_CHAT_MSG_SESS ON MEGAG_AI_CHAT_MENSAGENS(ID_SESSAO, CRIADO_EM);

